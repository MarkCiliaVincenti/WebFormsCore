namespace WebFormsCore.UI.WebControls;

/// <summary>Specifies the behavior mode of the text box.</summary>
public enum TextBoxMode
{
    /// <summary>Represents single-line entry mode.</summary>
    SingleLine,
    /// <summary>Represents multiline entry mode.</summary>
    MultiLine,
    /// <summary>Represents password entry mode.</summary>
    Password,
    /// <summary>Represents color entry mode.</summary>
    Color,
    /// <summary>Represents date entry mode.</summary>
    Date,
    /// <summary>Represents date-time entry mode.</summary>
    DateTime,
    /// <summary>Represents local date-time entry mode.</summary>
    DateTimeLocal,
    /// <summary>Represents email address entry mode.</summary>
    Email,
    /// <summary>Represents month entry mode.</summary>
    Month,
    /// <summary>Represents number entry mode.</summary>
    Number,
    /// <summary>Represents numeric range entry mode.</summary>
    Range,
    /// <summary>Represents search string entry mode.</summary>
    Search,
    /// <summary>Represents phone number entry mode.</summary>
    Phone,
    /// <summary>Represents time entry mode.</summary>
    Time,
    /// <summary>Represents URL entry mode.</summary>
    Url,
    /// <summary>Represents week entry mode. </summary>
    Week,
}
